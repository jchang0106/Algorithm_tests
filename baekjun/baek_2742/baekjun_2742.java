import java.util.*;

// Main class for BaekJoon website
class baekjun_2742 {

	public static void main(String[] args) {
		
		// Scanner for user input
		Scanner scan = new Scanner(System.in);
		
		// Natural number n
		int n = scan.nextInt();

		// Print from 1 to N
		for (int i = n; i > 0; i--){
			System.out.println(i);
		}


	}

}

